                <h1>Aloo Gobi Masala</h1>
                <p></p><h5><span class="label label-primary"> Method: </span></h5>
                <p class="description">1.Heat 2 teaspoon of oil in a pan on medium heat. Add cauliflower florets and fry for 2-3 minutes and then add the sliced potatoes.</p>
                <p class="description">2.Fry on medium-low flame for 7-8 minutes till potatoes and cauliflower have some brown spots on them.</p>
                <p class="description">3.Drain on a tissue paper and set aside.In the same pan heat 1.5 teaspoon of oil on medium heat and add cumin seeds and let them crackle.</p>
                <p class="description">4.Add the onions and cook for 2 minutes or till onion is translucent.Add the ginger-garlic paste.</p>
                <p class="description">5.Cook for another 2 minutes or till the raw smell of ginger and garlic goes away.Add the chopped tomatoes.Cook for 2 minutes till tomatoes are little soft.</p>
                <p class="description">6.Once the tomatoes are little soft, add turmeric powder, red chilli powder, coriander powder and amchur (mango powder). Cover the pan and let the masala cook for 2-3 minutes.</p>
                <p class="description">7.Now add the potatoes and cauliflower to it and mix.Add chopped coriander leaves and give a good mix.</p>
                <p class="description">8.Add some garam masala and cook the potato and cauliflower on medium-low heat for 5-6 minutes.</p>
                <p class="description">9.Add salt and cover the pan and cook for additional 6-7 minutes on low flame or till the potato and cauliflower are tender but not soggy. If you feel the masala is sticking, you may add some water. Add 1 tablespoon at a time and only add enough to cook the veggies. I did not add any water in mine.</p>
                <p class="description">10.Garnish with some more coriander leaves.</p>
                <p class="description">11.Serve aloo gobi with naan, paratha or roti.</p>
                <p class="description"><ul><li>* Add lots of coriander leaves to your aloo gobi, it adds a lot to the flavor.</li>

                    <li>* You can even deep fry the potatoes and cauliflower till they are completely cooked and then add them to the masala. But deep frying = more calories so it’s not the way I make aloo-gobhi at home regularly.</li>

                    <li>* You can cook the dish with ghee (clarified butter) or even butter.</li>

                    <li>* Adjust spice levels to taste.</li>

                    <li>* You can also sprinkle some kasuri methi (dried fenugreek leaves) on top for added flavor.</li></ul></p>
                <h5><span class="label label-primary"> Tips: </span></h5>
                <p class="description">1. Half cook the cauliflower and potatoes before adding it to the onion-tomato masala. You can parboil the potatoes and cauliflower or do what I did – fry them in little oil till they are half cooked. This helps in retaining the shape and texture.</p>
                <p class="description">2.Don’t cover and cook the cauliflower, you can cover towards the end and cook on low flame only.</p>
                <p class="description">3.Add salt towards the end when the dish is almost done.</p>
                
