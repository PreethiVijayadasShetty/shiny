            <p><ul><li>Kadai Paneer is a popular paneer recipe where paneer and bell peppers are cooked in a spicy masala.</li>

<li>It is one of the most popular paneer recipes. It’s there on the menu in almost all Indian restaurants. I remember always ordering it when we ate out.</li>

<li>Kadai is a like a wok commonly used in Indian cooking. We use it to make stir fry and all sorts of things. We also use it for deep frying snacks and sweets.</li>

<li>This dish is called Kadai Paneer because it’s cooked in a kadai. But if you don’t have a kadai you can simply use a wok or any other pan. I actually did not make it in a kadai!</li></ul></p>
