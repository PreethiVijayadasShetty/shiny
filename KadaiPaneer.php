                <h1>Kadai Paneer</h1>
                <p></p>
                <h5><span class="label label-primary"> Method : </span></h5>
                <p class="description">1. To a small kadai or pan, dry roast the spices for kadai masala for 3 to 4 minutes on medium heat until fragrant.   (Add more red chilies for a spicier masala).</p>
                <p class="description">2.Remove kadai from heat and transfer the roasted spices to a spice grinder.</p>
                <p class="description">3.Let it cool down a bit and then grind to a fine powder. Set aside. You can also make this kadai masala in advance and store it.</p>
                <p class="description">4. In a large kadai or any other pan, now melt butter with oil on medium heat.</p>
                <p class="description"> Prepare the curry:</p>
                <p class="description">5. Add the chopped onion and saute for 3 minutes until softened. Then add the sliced green chili and ginger garlic paste and saute for another 1 to 2 minutes.</p>
                <p class="description">6. Then add the chopped tomatoes and tomato paste. Mix and cook for around 6 minutes until tomatoes are really soft.</p>
                <p class="description">7.Then add 3 to 4 teaspoons of the kadai masala that you prepared earlier (or add the whole thing if you like it spicy). Also add the garam masala, kashmiri red chili powder.</p>
                <p class="description">8.Add half of the ginger julienne. Stir the spices and cook for 30 seconds.</p>
                <p class="description">9.Then add 1/2 cup water and stir.</p>
                <p class="description">Prepare Tadka</p>
                <p class="description">10. Add cream, salt and sugar and mix. Cook for one minute.</p>
                <p class="description">11.Add in the paneer and bell peppers (capsicum) and mix. Cover the pan or kadai with a lid and cook for 5 to 6 minutes on medium heat until bell peppers are slightly softened. You only want them half cooked here</p>
                <p class="description">Crush kasuri methi and add to the pan.</p>
                </p>
