            <div class="col-lg-6">
                <p><b>Kadai Masala:</b></p>
                <p>2 tablespoons coriander seeds</p>
                <p>2 dried red chilies</p>
                <p>4 green cardamom</p>
                <p>1 teaspoon fennel seeds</p>
                <p>3 cloves</p>
            <p><b>Kadai Paneer:</b></p>
            <div class="col-lg-6">
                
                <p>1 tablespoon butter, unsalted</p>
                <p>1 tablespoon oil, 15 ml, I used avocado oil</p>
                <p>1 medium red onion, 150 grams, finely chopped</p>
                <p>1 green chili, sliced</p>
                <p>2 teaspoons ginger garlic paste</p>
                <p>3-4 medium tomatoes, 450 grams, finely chopped</p>
                <p>1 tablespoon tomato paste, optional</p>
                <p>1/4 teaspoon garam masala</p>
                <p>3/4 teaspoon kashmiri red chili powder</p>
                <p>1 inch ginger, julienned, divided</p>
                <p>1/2 cup water, 4 oz/120 ml</p>
                <p>2 tablespoons heavy cream, 30 ml</p>
                <p>3/4 teaspoon salt, or to taste</p>
                <p>1 teaspoon sugar</p>
                <p>1 large green bell pepper (capsicum), cut into 1-inch pieces</p>
                <p>225 grams paneer, cubed, if using store-bought paneer soak it in warm water for 20-25 minutes before using</p>
                <p>1 teaspoon kasuri methi, dried fenugreek leaves</p>
                <p>cilantro, to garnish</p>
                
